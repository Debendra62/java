package com.Basic_java_class_5;

public class BitwiseOperatorDemo {

	public static void main(String[] args) {
	  int num1=25;
	  int num2=10;
	  int num3=num1 & num2;
	  System.out.println("The bitwise and is "+num3 );
	  int num4=num1 | num2;
	  System.out.println("The bitwise or is "+num4);
	  int num5=num1 ^ num2;
	  System.out.println("The bitwise not is "+num5);
	  
	}
}
