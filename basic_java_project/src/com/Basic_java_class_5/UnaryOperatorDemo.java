package com.Basic_java_class_5;

public class UnaryOperatorDemo {
	
  	
  public static void main(String[] args) {
	int num1=10;
	System.out.println("Initial number is  "+num1);
	++num1;
	num1++;
	System.out.println("Final number is  "+num1);
	
	boolean num2=true;
	System.out.println("Final number is  "+!num2);

}
}
